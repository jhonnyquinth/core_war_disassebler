char	*ft_strcpy(char *dest, const char *src)
{
	char *dest1;

	dest1 = dest;
	while (*src != '\0')
	{
		*dest1 = *src;
		src++;
		dest1++;
	}
	*dest1 = *src;
	return (dest);
}
